CREATE SEQUENCE IF NOT EXISTS customers_public_id_seq START WITH 1000 INCREMENT BY 1 MINVALUE 1000;

CREATE TABLE IF NOT EXISTS "customers" (
  "id" uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  "public_id" INTEGER NOT NULL DEFAULT nextval('customers_public_id_seq'),
  "group_id" uuid,
  "tag_id" uuid,
  "first_name" varchar,
  "last_name" varchar,
  "full_name"  varchar,
  "middle_name" varchar,
  "phone" varchar[],
  "birthday" date,
  "gender" varchar,
  "marital_status" varchar,
  "primary_lang" varchar,
  "email" varchar,
  "tg_username" varchar,
  "facebook" varchar,
  "instagram" varchar,
  "is_sms_notify" bool,
  "is_phone_notify" bool,
  "is_social_notify" bool,
  "is_email_notify" bool,
  "balance" NUMERIC(10, 2) DEFAULT 0.00,
  "debt_amount" NUMERIC(10, 2) DEFAULT 0.00,
  "created_by" uuid,
  "updated_by" uuid,
  "deleted_by" uuid,
  "is_active" boolean NOT NULL DEFAULT true,
  "created_at" TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
  "updated_at" TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW()
);